# define dictionary 'user_0'
user_0 = {
    "username": "efermi",
    "first": "enrico",
    "last": "fermi",
}

# for loop that iterates over the key:value pairs in 'user_0' using the
# items() method and prints them neatly
for key, value in user_0.items():
    print(f"\nKey: {key}")
    print(f"Value: {value}")
